public class Numbers{
	public static void main(String args[]){
		int num0 = Integer.parseInt(args[0]);
		int num1 = Integer.parseInt(args[1]);
	
		System.out.println(num0 + num1);
		System.out.println(num0 - num1);
		System.out.println(num0 * num1);
		System.out.println(num0 / num1);
		System.out.println(num0 % num1);
	
	}

}
